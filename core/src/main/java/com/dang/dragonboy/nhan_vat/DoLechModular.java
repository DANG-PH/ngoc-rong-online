package com.dang.dragonboy.nhan_vat;

public class DoLechModular {
    public float lechThanX, lechThanY;
    public float lechDauX, lechDauY;

    public DoLechModular(float ltx, float lty, float ldx, float ldy) {
        this.lechThanX = ltx;
        this.lechThanY = lty;
        this.lechDauX = ldx;
        this.lechDauY = ldy;
    }
}

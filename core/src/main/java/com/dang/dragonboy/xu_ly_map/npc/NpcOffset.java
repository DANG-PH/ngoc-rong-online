package com.dang.dragonboy.xu_ly_map.npc;

public class NpcOffset {
    public float dauX, dauY;
    public float thanX, thanY;
    public float chanX, chanY;

    public NpcOffset(float dauX, float dauY, float thanX, float thanY, float chanX, float chanY) {
        this.dauX = dauX;
        this.dauY = dauY;
        this.thanX = thanX;
        this.thanY = thanY;
        this.chanX = chanX;
        this.chanY = chanY;
    }
}
